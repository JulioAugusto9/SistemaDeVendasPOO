    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int i;
        for (i=0; i<pedido.getCliente().getPedidos().size(); i++) {
            if (pedido.getCliente().getPedidos().get(i).getNumero().equals(pedido.getNumero())) {
                pedido.getCliente().getPedidos().remove(i);
                break;
            }
        }
        for (i=0; i<pedido.getVendedor().getPedidos().size(); i++) {
            if (pedido.getVendedor().getPedidos().get(i).getNumero().equals(pedido.getNumero())) {
                pedido.getVendedor().getPedidos().remove(i);
                break;
            }
        }
        for (i=pedido.getItensPedido().size()-1; i>=0; i--) {
            pedido.removeItemPedido(i);
        }
        cadPedido.remove(posPedido);
        itensPedidoAntesAlterar = null;
        posPedido = -1;
        pedido = null;
        ativarModoConsulta();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairActionPerformed
        int i;
        if (posPedido == -1 && pedido != null) {
            if (pedido.getCliente() != null) {
                for (i=0; i<pedido.getCliente().getPedidos().size(); i++) {
                    if (pedido.getCliente().getPedidos().get(i).getNumero().equals(pedido.getNumero())) {
                        pedido.getCliente().getPedidos().remove(i);
                        break;
                    }
                }
            }
            if (pedido.getVendedor() != null) {
                for (i=0; i<pedido.getVendedor().getPedidos().size(); i++) {
                if (pedido.getVendedor().getPedidos().get(i).getNumero().equals(pedido.getNumero())) {
                        pedido.getVendedor().getPedidos().remove(i);
                        break;
                    }
                }
            }
            for (i=0; i<pedido.getItensPedido().size(); i++) {
                pedido.removeItemPedido(i);
            }
        }
        else if (pedido != null) {
            for (i=pedido.getItensPedido().size()-1; i>=0; i--) {
                pedido.removeItemPedido(i);
            }
            
            for (i=0; i<itensPedidoAntesAlterar.size(); i++) {
                pedido.addItemPedido(
                        new ItemPedido(
                                itensPedidoAntesAlterar.get(i).getSequencia(),
                                itensPedidoAntesAlterar.get(i).getQtdeVendida(),
                                itensPedidoAntesAlterar.get(i).getProduto()
                        )
                );
            }
        }
        
        dispose();
    }//GEN-LAST:event_btnSairActionPerformed

    

