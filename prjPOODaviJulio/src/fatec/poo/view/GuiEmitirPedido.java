    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
        DecimalFormat formato  = new DecimalFormat("#,##0.00");
        if (txtQtdeVendida.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, 
                    "Quantidade vendida deve ser maior que zero!", 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            txtQtdeVendida.setText(null);
            txtQtdeVendida.requestFocus();
            return;
        }
        double qtdeVendida = Double.parseDouble(txtQtdeVendida.getText());
        if (qtdeVendida <= 0) {
            JOptionPane.showMessageDialog(null, 
                    "Quantidade vendida deve ser maior que zero!", 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            txtQtdeVendida.setText(null);
            txtQtdeVendida.requestFocus();
            return;
        }
        
        Produto produto = cadProd.get(posProd);
        if (qtdeVendida > produto.getQtdeEstoque()) {
            JOptionPane.showMessageDialog(null, 
                    "Quantidade em estoque menor que a quantidade a ser vendida!" +
                    "\nEstoque: " + produto.getQtdeEstoque() +
                    "\nQuantidade a ser vendida: " + qtdeVendida, 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            txtQtdeVendida.setText(null);
            txtQtdeVendida.requestFocus();
            return;
        }
        
        ItemPedido itemPedido = new ItemPedido(seqItemPed, qtdeVendida, produto);
        if (itemPedido.calcValor() > pedido.getCliente().getLimiteDisp()) {
            JOptionPane.showMessageDialog(null, 
                    "Cliente nÃ£o tem limite disponÃ­vel o suficiente" +
                    "para adicionar esta quantidade deste produto!" +
                    "\nLimite disponÃ­vel: " + pedido.getCliente().getLimiteDisp() +
                    "\nValor: " + itemPedido.calcValor(), 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            itemPedido.setQtdeVendida(0);
            txtQtdeVendida.setText(null);
            txtQtdeVendida.requestFocus();
            return;
        }
        
        pedido.addItemPedido(itemPedido);
        seqItemPed++;
               
        String linha[] = {
            produto.getCodigo(),
            produto.getDescricao(),
            formato.format(produto.getPreco()),
            formato.format(itemPedido.getQtdeVendida()),
            formato.format(itemPedido.calcValor())
        };
        modTblProd.addRow(linha);
        
        lblQtdeItensPed.setText(String.valueOf(modTblProd.getRowCount()));
        lblValorTotalPed.setText(formato.format(pedido.calcValorTotal()));
        
        btnRemoverItem.setEnabled(true);
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

    private void btnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItemActionPerformed
        DecimalFormat formato  = new DecimalFormat("#,##0.00");
        if (tblProduto.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "A linha nÃ£o foi selecionada", 
                    "Aviso", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        pedido.removeItemPedido(tblProduto.getSelectedRow());
        modTblProd.removeRow(tblProduto.getSelectedRow());
        
        lblValorTotalPed.setText(formato.format(pedido.calcValorTotal()));
        lblQtdeItensPed.setText(String.valueOf(modTblProd.getRowCount()));
        
        if (modTblProd.getRowCount() == 0)
                btnRemoverItem.setEnabled(false);
    }//GEN-LAST:event_btnRemoverItemActionPerformed

